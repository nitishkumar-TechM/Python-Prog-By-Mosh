# Comparision Operators

print(10 > 3)   # True
print(10 >= 3)   # True
print(10 < 20)   # True
print(10 <= 20)   # True
print(10 == 10)   # True
print(10 == "10")   # False
print(10 != "10")   # True

# with Strings
print("**********Strings************")
print("bag" > "apple")      #True
print("bag" == "BAG")       # False

# There is a inbuilt function for numeric representation of Character
# ord()
print(ord("b"))     #b => 98
print(ord("B"))     #B => 66