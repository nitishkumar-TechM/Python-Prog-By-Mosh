#String Slicing and Operations on String

name = "Nitish Kumar"

print(name[:6])
print(name[:6:2])
print(name[6:])
print(name[0:-3])
print(name[0: len(name)-3])

print(len(name))

print('-------------------')

fruit = "MangoBanana"

print('Length of fruit: ', len(fruit))

print(fruit[-1:-3])
print(fruit[len(fruit)-1: len(fruit)-3])
print(fruit[-3:-1])

